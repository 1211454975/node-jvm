public class Operators {
    public int sum(int one,int two){
        return one+two;

    }

    public int multiply(int one,int two){
        return one*two;

    }


    public int subtract(int one,int two){
        return one-two;

    }


    public int divide(int one,int two){
        return one/two;

    }

    public int remaind(int one,int two){
        return one % two;

    }


    public long sumLong(long one,long two){
        return one+two;

    }

    public long multiplyLong(long one,long two){
        return one*two;

    }


    public long subtractLong(long one,long two){
        return one-two;

    }


    public long divideLong(long one,long two){
        return one/two;

    }

    public long remaindLong(long one,long two){
        return one % two;

    }
}